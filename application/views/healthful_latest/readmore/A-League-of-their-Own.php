                <h3 class="align-center pb-3 mbr-fonts-style display-7">
                    Seven outstanding physicians feted at first CebuDoc Group Gala and Appreciation Night
                </h3>
                <div class="">
                    <div class="col-12 mbr-fonts-style  display-7 col-md-12" style="font-size: 1em;">
                        <p>
                            IT WAS A GLAMOROUS EVENING of dance, music, celebration and putting the spotlight on the best of the best as CebuDoc Group held its first ever Gala and Appreciation Night.
                        </p>

                        <p>
                            Marking another year of compassion, quality, innovation and growth as the leading healthcare institutions in Visayas and Mindanao, CebuDoc Group gathered more than 500 physicians of different specializations last September 2, 2019 at the Pacific Grand Ballroom of the Waterfront Hotel and Casino in Lahug, Cebu City. Taking center stage were seven excellent physicians who have dedicated their lives in service, inspiring many practitioners, especially the young generation.
                        </p>

                        <p>
                            <h3>
                                Outstanding Leadership: CDG Excellence Awards
                            </h3>
                            The CDG Excellence Awards were created to recognize the most dedicated physicians who have rendered outstanding service to CebuDoc Group and to the community. The prestigious award is given to doctors who have demonstrated exceptional skills in clinical practice and have shown commitment to the mission, vision, and values of CebuDoc Group.
                        </p>
                       <p>
                           For this year, each member-hospital of CebuDoc Group had one awardee. They are:
                       </p>
                        <ul style="text-align: left;">
                            <li class=""> Dr. Cherrie Mae S. Reynaldo, FPCP of OrmocDoc</li>
                            <li style="text-align: left;" class="">Dr. Fe D. Abella of SouthGen</li>
                            <li class="">Dr. Wilmer L. Yap, DPCP of SanCarlosDoc</li>
                            <li class="">Dr. Rose Marie M. Reyes, FPPS of NorthGen</li>
                            <li class="">Dr. Osita G. Nimis, FPAFP of MactanDoc</li>
                            <li class="">Dr. Rogelio V. Del Prado, DPBA, FPBA of CebuDoc</li>
                        </ul>
                        <p>
                            The seventh award — the Dr. Potenciano V. Larrazabal, Jr. Lifetime Achievement Award — was created to honor the memory of CebuDoc Group’s late founder and is given to a physician who has taken to heart the vision, mission, and core values of CebuDoc Group for the past 20 years or more. This year’s awardee is Dr. Manuel T. Lim, FPCP, FPCC.
                        </p>
                        <p>
                            <h3>Inspirational Stories Behind the Success</h3>
                        </p>
                        The seven honorees are proof that success cannot be achieved overnight. Each of them worked hard at learning and mastering their profession while serving as leader, mentor and inspiration to those who look up to them.

                        <p>
                            OrmocDoc’s Excellence Awardee Dr. Cherrie Mae S. Reynaldo practices Internal Medicine at Ormoc Doctors Hospital. She is the team leader of the Antimicrobial Stewardship Committee and is also part of OrmocDoc’s board of directors.
                        </p>
                        <p>
                            Described by colleagues and patients as very understanding and approachable, Dr. Reynaldo has this piece of advice to the younger generation of doctors: “Give your best care to the patients and never lose sight of the real reason why we are here and why we chose this profession.”
                        </p>
                        <p>
                            SouthGen Excellence Awardee Dr. Fe D. Abella’s field of specialization is Internal Medicine and she has been with the hospital since it was established in 2005.
                        </p>
                        <p>
                            “She is compassionate and observes strong work ethic. She treats her patients like her own family,” said Cathy Degoma, a patient of Dr. Abella.
                        </p>
                        <p>
                            Known for maintaining a harmonious relationship with her colleagues, she also listens to nurses and patients.
                        </p>
                        <p>
                            “The best reward we can get is to see our patients living well and living longer. I consider it as my second greatest contribution to humanity, next to my children, and day to day, I’m thankful to God for this wonderful blessing,” said Dr. Abella.
                        </p>
                       <p> 
                             SanCarlosDoc Excellence Awardee Dr. Wilmer L. Yap practices Internal Medicine at San Carlos Doctors Hospital. A medical director at SanCarlosDoc, Dr. Yap served as president of the San Carlos City Medical Society for four years, doing medical missions in the mountain barangays whose residents have poor access to health services. He also implemented a consignment system at the hospital’s pharmacy to make medicines more accessible to patients.
                        </p>
                        <p>
                            “He’s very compassionate and kind to his patients,” remarked a colleague about Dr. Yap. “He encourages young physicians to always treat your patients as if they’re your closest kin.”
                        </p>
                        <p>
                            NorthGen Excellence Awardee Dr. Rose Marie M. Reyes has been working as pediatrician at North General Hospital since it started operations in 1999. She also served as head adviser of Pediatrics Residents in Barangay Cabangahan in the northern town of Consolacion, Cebu.
                        </p>
                        <p>
                            “Being a doctor is a commitment to serve. You have to be dedicated to your work,” said Dr. Reyes.
                        </p>
                        <p>
                            MactanDoc Excellence Awardee Dr. Osita G. Nimis, practices Family Medicine and has been with the hospital since it was established in 1993.
                        </p>
                        <p>
                            Dr. Nimis is the founding chairman of the MactanDoc Department of Family Medicine, which was granted full accreditation under her leadership until the year 2005.
                        </p>
                        <p>                             
                            “Love your work, yourself and your patients and you’ll never get tired,” Dr. Nimis said.
                        </p>
                        <p>
                            A true icon for excellence, CebuDoc Excellence Awardee Dr. Rogelio V. Del Prado has been with Cebu Doctors University Hospital since it was founded in 1972. He has been practicing his profession as an anesthesiologist for almost 50 years now.
                        </p>
                        <p  id="NewEra">
                            “He has been a dedicated consultant who faithfully fulfills his duties and responsibilities, which include teaching the resident staff,” said a colleague of Dr. del Prado.
                        </p>
                        <p>
                            “If you care for your patients, you give them reassurance. For me, reassurance is much more effective than a thousand pills of sedatives,” remarked Dr. del Prado.
                        </p>
                        <p>
                            <h3>The New Era of CebuDoc Group</h3>
                            Celebrating another milestone, CebuDoc Group has paved the way for the growth of a network of reputable medical institutions in Ormoc City, San Carlos City, Lapu-Lapu City, Naga City, Cebu City and soon in Bohol and Danao City. CebuDoc Group is now at the forefront of the healthcare industry in the country, continuously providing world-class quality and excellent healthcare services. Its member-hospitals are all set for upcoming upgrades and expansions all geared towards fulfilling CebuDoc Group’s vision to cater to the increasing population and health care demand in Central Visayas.
                        </p>
