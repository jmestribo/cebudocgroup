                <p class="mbr-text mbr-fonts-style display-7"><strong>Radiation Oncology (sometimes called radiation therapy or radiotherapy)</strong> is the medical use of radiation in the treatment of various diseases, primarily cancer. At Cebu Doctors' University Hospital - Radiation Therapy Center (CDUH-RTC) the treatments are given by a machine called a <strong>Medical Linear Accelerator (LINAC).</strong> The radiation produced by the linac is computer controlled to destroy abnormal cells while preserving the normal, surrounding tissue. In many cases, radiation therapy is the single best method for the treatment of diseases such as cancer, but it also may be combined with surgery and/or chemotherapy.<br><br>

