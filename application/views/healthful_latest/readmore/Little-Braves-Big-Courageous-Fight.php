                  Little Brave’s Big Courageous Fight
                </h2>
                <br>
                <div class="">
                     <div class="col-12 mbr-fonts-style  display-7 col-md-12" style="font-size:1em;">
                        <p>
                         CHILD IS THAT ONE IMPORTANT PIECE OF THE PUZZLE a family. With its joys and challenges, parenthood can sometimes cause extreme feelings of happiness and anxiety, excitement and fear, especially when one is experiencing it for the first time.

                        </p>

                        <p>
                        Kimberly Yburan, a staff nurse at Mactan Doctors Hospital, was a picture of joy and contentment when she gave birth to her first child—a bouncing baby boy—last June 23. Her husband, Luem had picked the name Brave for their firstborn. But the couple had no idea that the choice of name would be significant in their lives as new parents.
                        </p>

                        <p>
                         The happiness Kimberly and Luem felt upon seeing their child turned into apprehension and sadness when they found out that Brave was unwell.
                        </p>

                        <p>
                          A couple of days after being born, the healthy little boy turned into a blanched, ailing infant. He would be sleeping longer than he was awake, and feeding time gradually became a challenge. He also exhibited unusual hand gestures.
                        </p>

                        <p>
                          One day after a regular breastfeeding session, Brave’s breathing became alarmingly slow, his complexion turned pale and his lips became blue. Around 9PM of July 4, the little boy was rushed to the hospital.
                        </p>

                        <p>
                          Initial findings pointed to aspiration pneumonia and neonatal sepsis. With antibiotics and further testing, Brave was transferred to an intensive care unit (ICU) for close monitoring.
                        </p>

                        <p>
                         Brave’s appetite worsened, with his small body getting weaker. His oxygen status would drop from time to time, prompting the doctors to do intubation, and prohibiting any feeding for a few hours more.
                        </p>

                        <p>
                         Seeing their son in that disheartening state, the Yburan couple’s hope that their child would recover became dimmer.
                        </p>

                        <p>
                            But little Brave was a strong fighter. After going through a battery of blood tests, increased levels of medications, and a spinal tap, he showed his parents that they had no reason to give up.
                        </p>
                        <p>
                            Hours after intubation, the little boy startled everyone by waking up livelier and more responsive than before. Extubation was ordered after he showed signs of improvement. Another batch of tests was run and results came back all clear.
                        </p>
                        <p>
                            All but one remained normal. His breathing pattern resembled that of an adult, which is slower than a normal infant. Kimberly was told her baby was still not okay. Brave had to stay in the ICU a couple of days more.
                        </p>
                        <p>
                            One early morning in mid- July, Kimberly was informed to wait for a specific call later that afternoon. Clueless of what to expect, the new mother held on to one bit of hope, fervently praying that her strong faith would keep her going.
                        </p>
                        <p>
                            
                            Meanwhile, Brave’s skin began to turn pale. He started showing signs of difficulty in breathing and had to be intubated again. Just in time, the attending physician arrived with the news they all had been waiting for.
                        </p>

                        <p>
                            
                           “MSUD jud siya, Kim,” he said. Silence filled the room. MSUD or Maple Syrup Urine Disease is an inherited disorder in which the body is unable to process certain protein building blocks (amino acids) properly.
                            With what the doctor said, the parents finally knew why Brave would be lively when not fed with milk but would get sick after breastfeeding.

                        </p>
                        <p>
                            Concerned and confused, the parents were referred to a geneticist for counselling. Right after the diagnosis, Brave was scheduled for a peritoneal dialysis. His whole diet was changed and all other medical management was adjusted. A few days after, they were finally sent home.
                        </p>

                        <p>
                            With a special milk formula and strict diet, Brave is now a bright and healthy child. After everything they went through, the Yburan couple realized that their faith in God and in their extraordinary baby was strong enough for little Brave not to give up on his own fight for life.
                        </p>
                    
