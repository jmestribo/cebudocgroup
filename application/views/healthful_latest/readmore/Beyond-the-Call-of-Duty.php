                        <p>
                            A man in his 80s was gathering water from a deep well. When he was done, he proceeded to cross the street but was hit by a speeding motorcycle. The driver immediately fled the scene, leaving his poor victim.

                        </p>

                        <p>
                            Concerned witnesses sought for help. One of them headed towards a house right across the street where the accident happened. Fortunately, Mia Olorvida, a newly registered nurse working at Ormoc Doctors Hospital, was home. The witness called Mia for help. Although startled, Mia immediately took out her Community Health Nursing (CHN) kit to assess the injuries of the victim and provide him with first aid. The old man was then rushed to OrmocDoc. He sustained multiple physical injuries, including a bone fracture and a dislocated shoulder.

                        </p>

