                                <div class="tab-pane fade" id="v-pills-d" role="tabpanel" aria-labelledby="v-pills-d-tab">
                                    <h5>D. WHAT TO DO WHEN IN LABOR</h5>
                            <ul class="mbr-text mbr-fonts-style display-7" style="list-style-type: circle;">
                                <li>
                                    Watch out for signs of labor:
                                        <ul class="mbr-text mbr-fonts-style display-7" style="list-style-type: circle;">
                                            <li>
                                                Strong, frequent regular cramps on abdomen and lower back
                                            </li>
                                            <li>
                                                Passage of blood tinged vaginal discharge
                                            </li>
                                            <li>
                                                Passage of watery vaginal discharge (needs immediate admission)
                                            </li>
                                        </ul>
                                </li>

                                <li>
                                    As soon as labor begins, you may take a quick shower and come to CDUH immediately.
                                </li>

                                <li>
                                    Bring toilet articles, baby's layette, medicines, prescription given to OPD, OB white card and certificate of blood donation.
                                </li>
                            </ul>
                                </div>

                                <div class="tab-pane fade" id="v-pills-e" role="tabpanel" aria-labelledby="v-pills-e-tab">
                                    <h5>E. WHAT TO EXPECT</h5>
                            <ol>
                                <li>
                                    <strong>DURING DELIVERY</strong>
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>The patient stays in the labor room until she is ready to deliver.</li>
                                            <li>Unauthorized persons are not allowed inside the Labor/Delivery Room Complex</li>
                                        </ul>
                                </li>
                                <li>
                                    <strong>AFTER DELIVERY</strong>
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>After delivery, the patient shall be observed for at least two hours in the delivery room for early detection of bleeding, etc.</li>
                                            <li>CDUH is a Mother-Baby Friendly Hospital, The baby shall be roomed-in two hours after deliver.</li>
                                            <li>The baby should be breastfed unless medically contraindicated.</li>
                                            <li>All PMS babies are mandated by Department of Health to have the following procedures within twelve hours of birth. Cost must be paid for by the patien.</li>
                                        </ul>

                                        <table class="table table-striped">
                                            <tr>
                                                <td>Expanded Newborn Screening</td>
