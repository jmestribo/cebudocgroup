                    <li>Painless lump in the breast</li>
                    <li>A change in the size or shape of the breast</li>
                    <li>Nipple discharge or tenderness, or the nipple pulled back (inverted) into the breast.</li>
                    <li>Ridges or pitting of the breast, making the skin look like the skin of an orange</li>
                    <li>A change in the look or feel of the skin of the breast, areola or nipple such as warmth, swelling, redness.</li>
