                                        </table>
                                </li>
                                <li>
                                    <strong>Upon Discharge</strong>
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>The patient has to be examined by the Resident-on-duty before discharge.</li>
                                            <li>The patient can ask the resident/nurse on duty for advice regarding the care of episiotomy wound and the baby's umbilical cord</li>
                                            <li><strong>Official Receipts</strong> of the Prenatal Advance Deposit (PAD) should be presented  to the cashier so that these can be deducted from the hospital bill.</li>
