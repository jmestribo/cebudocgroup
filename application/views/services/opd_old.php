                                    <h5>A. GENERAL GUIDELINES</h5>
                            <ol>
                                <li>
                                    Pregnancy up to <strong>30</strong> weeks <strong>6/7</strong> days is accepted to the service. (Special Considerations maybe made regarding the acceptance into the program with the approval of the Department Chairman &amp; Training Officer).
                                </li>

                                <li>
                                    Upon enrollment, the enrolle should present a photocopy of <strong>valid ID</strong>. In addition, enrollee's husband/partner or parents if applicable is also required to submit a photocopy of <strong>valid ID</strong>
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li><strong>Certificate of employment</strong> should be submitted for enrollees with spouses working abroad.</li>
                                        </ul>
                                </li>

                                <li>
                                    Schedule of prenatal care visit must be strictly followed:
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>
                                                <strong>First visit schedule</strong> is every:
                                                <ul class="mbr-text mbr-fonts-style display-7">
                                                    <li> <strong>8:00 - 10:00 AM</strong> : Monday, Tuesday, Wednesday & Saturday</li>
                                                    <li> <strong>1:00 - 3:00 PM</strong> : Monday & Wednesday</li>
                                                </ul>
                                            </li>
                                            <li>
                                                Succeeding visits schedule shall be determined by the attending resident physician.
                                            </li>
                                        </ul>
                                </li>
                                
                                <li>
                                     <strong>Patients are required to attend the PMS orientation within the first two weeks after the first prenatal check-up.</strong>
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>
                                                <strong>PMS orientation chedule</strong> is every:
                                                    <ul class="mbr-text mbr-fonts-style display-7">
                                                        <li> <strong>8:00 AM</strong> only - Wednesday & Saturday</li>
                                                    </ul>
                                                    
                                                    <div class="bg-info p-2">
                                                        <em>
                                                            <strong>
                                                                Please come on time, late comers will not be accommodated.
                                                            </strong>
                                                        </em>
                                                    </div>
                                            </li>

                                            <li>
                                                One immediate family with photocopy of a <strong>valid ID</strong> is required to attend during the orientation.
                                            </li>

                                            <li>
                                                For unmarried enrolless <strong>below 21 yrs. old</strong> either of the <strong>father, mother, siblings of legal age</strong> with a photocopy of <strong>valid ID</strong> is required to attend during the orientation.
                                            </li>

                                            <li>
                                                 Failure to attend the <strong>PMS orientation</strong> would automatically disqualify the enrollee from the <strong>PMS</strong>.
                                            </li>

                                            <li>
                                                All PMS patients are required to attend two (2) sessions of breastfeeding class before delivery.
                                            </li>
                                        </ul>
                                </li>

                                <li>
                                    Prenatal care check-ups & deliveries shall be handled by the <strong>OB Resident physicicians &amp; PGIs.</strong>
                                </li>

                                <li>
                                    Donation of <strong>one (1) unit of blood</strong> is required before the patient's discharge from the hospital
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>
                                                Failure to donate blood upon discharge shall cause the enrollee to be considered as <strong>House Case</strong> under a <strong>private physician</strong> & be billed as such. Prenatal advance deposit <strong>(PAD) & PMS benefits</strong> shall be forfeited.
                                            </li>
                                        </ul>
                                </li>

                                <li>
                                    Patients who stay beyond the prescribed <strong>3 days</strong> for normal delivery & <strong>5 days</strong> for caesarean section would be charged for the excess room accommodation.
                                </li>

                                <li>
                                    Erolled privileged mothers who delivered outside CDUH shall <strong>forfeit</strong> all fees paid up during their prenatal care visits.
                                </li>

                                <li>
                                    Erollees shall be <strong>disqualified</strong> for PMS admission in the event of the following reasons:
                                        <ul class="mbr-text mbr-fonts-style display-7">
                                            <li>
                                                No certificate of blood donation upon discharge from the hospital.
                                            </li>

                                            <li>
                                                Non-compliance to scheduled prenatal care visits and/or not following physician's order is subject for review by the chief resident fo deliberation.
                                            </li>
                                        </ul>
                                </li>

                                <li>
                                    For non-delivery related hospitalizations/pre-mature urine contraction, patient must make a separte blood donation to avail service discount prior to hospital discharge. Otherwise will be considered as house case/private patient.
                                </li>

                                <li>
                                    During admission patient shall pay for medicines, supplies and other consumables used before, during and after delivery.
                                </li>

                                <li>
                                    Room accomodation shall be in the <strong>OB  ward</strong> located at the 4th floor of the hospital.
                                </li>

                                <li>
                                    Only <strong>one (1)</strong> watcher is allowed to accompany the patient throughout the hsopital stay.
                                </li>

                                <li>
                                    PHIC members may qualify under this program; <strong>Health Insurance benficiaries are excluded form the program.</strong>
                                </li>

                                <li>
                                    Patients discharged againts medical advice shall not be <strong>accepted</strong> again under PMS.
                                </li>
                            </ol>
