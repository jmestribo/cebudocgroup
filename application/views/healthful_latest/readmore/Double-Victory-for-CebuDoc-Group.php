                <h2 class="mbr-fonts-style  align-center  display-2">Double Victory for CebuDoc Group</h2>
                <h3 class="align-center pb-3 mbr-fonts-style display-7">
                    CebuDoc and SouthGen awarded Most Outstanding
                    Hospitals by the Philippine Hospital Association
                </h3>
                <div class="">
                    <div class="col-12 mbr-fonts-style  display-7 col-md-12" style="font-size: 1em;">
                        <p>
                           VICTORY IS TWICE AS SWEET FOR CEBUDOC GROUP AS TWO OF ITS MEMBER-HOSPITALS, Cebu Doctors University Hospital and South General Hospital, were awarded as 2019 Most Outstanding Hospital-Level 3, Private and 2019 Most Outstanding Hospital- Level 2, Private, respectively by the Philippine Hospital Association (PHA) last November 20 at the Manila Hotel, Manila Philippines.
                        </p>
                        <p>
                            It was the second time in a row that CebuDoc won the award, having been given the same recognition last year. It was SouthGen’s first time to clinch the award.
                        </p>
                        <p>
                           CebuDoc and SouthGen were recognized on the basis of excellence in the fields of service, training, research, and governance, serving as an inspiration to almost 2,000 memberhospitals from both the private and government sectors. The awarding was done during PHA’s 70th Annual National Convention.
                        </p>
                        <p>
                            PHA gave out three coveted awards, namely the Most Outstanding Hospital Award, the Most Outstanding Chapter, and the Most Outstanding Council Award after a thorough evaluation and assessment of its member-hospitals.
                        </p>
                        <p>
                            The judging was based on the different categories such as Corporate Social Responsibility, Excellence in Governance, Accreditations, Infection Control, Disaster Preparedness, Green Hospital Initiative, Social and Sports Development Program, Staff Meetings, Safety and Patient Care, Continuity of Care, Patient Engagement and Education, and Transparency in reporting Sentinel Events of adverse reactions.
                        </p>
                        <p>
                           “There are 10 criteria and CebuDoc and SouthGen gave us complete evidence for all criteria. We do this award every year to recognize which hospital has excellent healthcare service and we try to excel in and improve our service for better healthcare,” said Dr. Edgardo V. Salud, Chairman of the PHA Most Outstanding Awards Committee.
                        </p>
                        <p>
                           “Recognition from PHA means that not only are we on the right path, we are also trailblazers for quality healthcare. Our services, trainings, researches, and governing bodies serve as motivation and inspiration to almost 2,000 hospitals that are part of the PHA,” said Karen Alfafara, SouthGen Hospital Administrator.
                        </p>
                        <p>
                            <h3>
                                Advancement of CebuDoc Group
                            </h3>
                           <p> Over the past years, CebuDoc Group has continuously delivered its commitment to provide compassionate, high quality, and innovative service.
                            </p>
                           <p> “In our six hospital brands, we have geared our team with the most advanced and modern healthcare technology as we prepare for the rapid growth of the healthcare industry,” said Engr. Huriel C. Alejandro, Corporate Administrator.
                            </p>
                            <p> “For this win, everybody deserves a pat on the back—from our maintenance staff to the medical team. United we stand, that is why we never fall,” said Dr. Yong Larrazabal III, President and Chairman of CebuDoc Group.</p>
                        </p>
